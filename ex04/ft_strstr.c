char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	if (*to_find == '\0')
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			j = 0;
			while (to_find[j] != '\0' && to_find[j] == str[i + j])
				j += 1;
			if (to_find[j] == '\0')
				return (&str[i]);
		}
		i += 1;
	}
	return (0);
}
